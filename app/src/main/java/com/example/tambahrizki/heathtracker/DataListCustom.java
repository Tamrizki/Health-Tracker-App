package com.example.tambahrizki.heathtracker;

public class DataListCustom {
    int gambar;
    String judul;

    public DataListCustom(int gambar, String judul) {
        this.gambar = gambar;
        this.judul = judul;
    }

    public int getGambar() {
        return gambar;
    }


    public String getJudul() {
        return judul;
    }

}
